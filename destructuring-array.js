/**
 * @file destructuring Assigment Array
 *
 * Descructuring Assignment Array allows extract data from Array to the the variables
 *
 * @see example
 */

"use strict";

let name = ['Ranjeet', 'kumar', 'Mahto'];

// assign array elements to the variables
let  [firstName, middleName, lastName] = name;

console.log(firstName, middleName, lastName);
//output Ranjeet kumar mahto